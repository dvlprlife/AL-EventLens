    '.right { flex: 1; min-width: 200px; padding: 12px; overflow: auto; }',
    '.filters { display: flex; gap: 4px; margin: 8px 8px 0 8px; }',
    '.filters select { flex: 1; min-width: 0; padding: 4px 6px; background: var(--vscode-input-background); color: var(--vscode-input-foreground); border: 1px solid var(--vscode-input-border); font: inherit; outline: none; }',
    '#search { margin: 4px 8px 8px 8px; padding: 6px 8px; background: var(--vscode-input-background); color: var(--vscode-input-foreground); border: 1px solid var(--vscode-input-border); font: inherit; outline: none; }',
    '.list { list-style: none; margin: 0; padding: 0; flex: 1; overflow-y: auto; }',
