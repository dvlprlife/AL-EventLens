    '    subscribers = m.subscribers || [];',
    '    appMetaMap = new Map(m.appMeta || []);',
    '    rebuildSubscribersIndex();',
    '    populateFilters();',
    '    render();',
    "  } else if (m.type === 'select' && m.publisher) {",
    "    if (mode !== 'publishers') { setMode('publishers'); }",
    '    const newKey = keyOf(m.publisher);',
    '    const newLi = findLiByKey(newKey);',
    '    setSelected(newKey, newLi);',
    '    scrollSelectedIntoView();',
    "  } else if (m.type === 'reveal' && typeof m.search === 'string') {",
    "    // Reveal: external trigger (tree click, CodeLens) sets the search box,",
    '    // re-renders the filtered list, and optionally selects + scrolls a',
    '    // specific publisher inside the now-filtered view.',
    "    const searchEl = document.getElementById('search');",
    '    searchEl.value = m.search;',
    "    if (typeof m.selectKey === 'string') {",
    '      selectedKey = m.selectKey;',
    '    }',
    "    if (mode !== 'publishers') { setMode('publishers'); } else { render(); }",
    "    if (typeof m.selectKey === 'string') {",
    '      scrollSelectedIntoView();',
    '    }',
    "  } else if (m.type === 'revealSubscriber' && m.subscriber) {",
