    "  document.getElementById('subscribers').hidden = !subscribersMode;",
    "  document.getElementById('search').placeholder = subscribersMode",
    "    ? 'Search subscribers... (try app:Foo kind:codeunit)'",
    "    : 'Search publishers... (try app:Foo kind:codeunit)';",
    '  populateFilters();',
    '  render();',
    '}',
